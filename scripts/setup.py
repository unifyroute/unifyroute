# ── Helpers ────────────────────────────────────────────────────────────────────

def c(code: str, text: str) -> str:
    """ANSI colour helper."""
    CODES = {"red": "31", "green": "32", "yellow": "33", "cyan": "36", "bold": "1", "reset": "0"}
    return f"\033[{CODES.get(code, '0')}m{text}\033[0m"


def banner(text: str):
    print("\n" + c("cyan", "─" * 60))
    print(c("bold", f"  {text}"))
    print(c("cyan", "─" * 60))


def ok(msg: str):   print(c("green", f"  ✅ {msg}"))
def warn(msg: str): print(c("yellow", f"  ⚠️  {msg}"))
def err(msg: str):  print(c("red", f"  ❌ {msg}"))
def info(msg: str): print(f"  ℹ️  {msg}")


def ask(prompt: str, default: str = "", secret: bool = False) -> str:
    """Prompt user for input with an optional default."""
    hint = f" [{default}]" if default else ""
    full_prompt = f"  {prompt}{hint}: "
    if secret:
        import getpass
        val = getpass.getpass(full_prompt)
    else:
        val = input(full_prompt)
    return val.strip() or default


def ask_bool(prompt: str, default: bool = True) -> bool:
    default_hint = "Y/n" if default else "y/N"
    val = input(f"  {prompt} [{default_hint}]: ").strip().lower()
    if not val:
        return default
    return val in ("y", "yes")


def run(cmd: list[str], cwd: Path = ROOT, check: bool = True) -> int:
    """Run a command, streaming output."""
    print(f"  $ {' '.join(cmd)}")
    result = subprocess.run(cmd, cwd=str(cwd))
    if check and result.returncode != 0:
        err(f"Command failed with exit code {result.returncode}")
        sys.exit(result.returncode)
    return result.returncode


def set_env_var(key: str, value: str, env_content: dict) -> dict:
    """Update a key in the env content dict."""
    env_content[key] = value
    return env_content


def read_env_file(path: Path) -> dict[str, str]:
    """Parse a .env file into a dict (preserving comments as special keys)."""
    result: dict[str, str] = {}
    if not path.exists():
        return result
    for line in path.read_text().splitlines():
        stripped = line.strip()
        if stripped.startswith("#") or not stripped:
            continue
        if "=" in stripped:
            key, _, val = stripped.partition("=")
            result[key.strip()] = val.strip()
    return result


def write_env_file(path: Path, config: dict[str, str], header: str = ""):
    """Write a dict to a .env file in a readable grouped format."""
    lines = []
    if header:
        lines.append(f"# Generated by unifyroute setup on {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
        lines.append("")

    # Group output for readability
    groups = {
        "# ── Database ──────────────────────────────────────────────────────────────": [
            "DB_BACKEND", "SQLITE_PATH",
        ],
        "# ── Application ────────────────────────────────────────────────────────────": [
            "PORT", "HOST", "API_BASE_URL",
        ],
        "# ── Redis ───────────────────────────────────────────────────────────────────": [
            "REDIS_URL",
        ],
        "# ── Secrets ─────────────────────────────────────────────────────────────────": [
            "MASTER_PASSWORD", "VAULT_MASTER_KEY", "JWT_SECRET",
        ],
    }

    written = set()
    for section_header, keys in groups.items():
        section_lines = []
        for k in keys:
            if k in config:
                section_lines.append(f"{k}={config[k]}")
                written.add(k)
        if section_lines:
            lines.append(section_header)
            lines.extend(section_lines)
            lines.append("")

    # Write remaining keys not in any group
    remaining = [(k, v) for k, v in config.items() if k not in written]
    if remaining:
        lines.append("# ── Other ──────────────────────────────────────────────────────────────────")
        for k, v in remaining:
            lines.append(f"{k}={v}")
        lines.append("")

    path.write_text("\n".join(lines), encoding="utf-8")


def backup_config(reason: str = ""):
    """Backup the current .env file and SQLite database."""
    ts = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
    backed_up = []

    if ENV_FILE.exists():
        env_backup = ROOT / f".env.backup.{ts}"
        shutil.copy(ENV_FILE, env_backup)
        backed_up.append(env_backup.name)
    else:
        info("No .env to back up.")

    db_file = ROOT / "data" / "unifyroute.db"
    if db_file.exists():
        db_backup = ROOT / f".db.backup.{ts}"
        shutil.copy(db_file, db_backup)
        backed_up.append(db_backup.name)
    else:
        info("No database to back up.")
        
    if backed_up:
        reason_str = f" ({reason})" if reason else ""
        ok(f"Backed up: {', '.join(backed_up)}{reason_str}")
        return ts
    return None


def find_saved_configs() -> list[str]:
    """Find all backup timestamps in the project root, newest first.
    A valid backup has at least a .env.backup.* file.
    """
    backups = sorted(
        ROOT.glob(".env.backup.*"),
        key=lambda p: p.stat().st_mtime,
        reverse=True,
    )
    # Return just the timestamp part
    return [p.name.replace(".env.backup.", "") for p in backups]


def restore_config(ts: str) -> dict[str, str]:
    """Restore configuration and database from a backup timestamp.

    Copies the backup files to their original locations.
    """
    env_backup = ROOT / f".env.backup.{ts}"
    if env_backup.exists():
        shutil.copy(env_backup, ENV_FILE)
        config = read_env_file(ENV_FILE)
        ok(f"Configuration restored from {env_backup.name}")
        
        # Show restored values (mask secrets)
        secret_keys = {"MASTER_PASSWORD", "VAULT_MASTER_KEY", "JWT_SECRET"}
        for key, val in config.items():
            if key in secret_keys:
                display = val[:3] + "***" if len(val) > 3 else "***"
            else:
                display = val
            info(f"  {key} = {display}")
    else:
        config = {}

    db_backup = ROOT / f".db.backup.{ts}"
    if db_backup.exists():
        data_dir = ROOT / "data"
        data_dir.mkdir(exist_ok=True)
        db_file = data_dir / "unifyroute.db"
        shutil.copy(db_backup, db_file)
        ok(f"Database restored from {db_backup.name}")

    return config


def find_uv() -> str:
    """Find the uv binary."""
    uv = shutil.which("uv")
    if uv:
        return uv
    home_uv = Path.home() / ".local" / "bin" / "uv"
    if home_uv.exists():
        return str(home_uv)
    err("'uv' is not installed. Install with:\n    curl -LsSf https://astral.sh/uv/install.sh | sh")
    sys.exit(1)


def find_npm() -> str:
    npm = shutil.which("npm")
    if not npm:
        err("'npm' is not installed. Please install Node.js.")
        sys.exit(1)
    return npm


def find_docker() -> bool:
    docker = shutil.which("docker")
    if not docker:
        return False
    result = subprocess.run(["docker", "info"], capture_output=True)
    return result.returncode == 0


def check_prerequisites() -> bool:
    """
    Run the prerequisite checker and exit if requirements aren't met.
    Returns True if all prerequisites are satisfied, exits with error if not.
    """
    banner("Checking Prerequisites")
    
    check_script = SCRIPT_DIR / "check_prerequisites.py"
    if not check_script.exists():
        err(f"Prerequisite check script not found at {check_script}")
        sys.exit(1)
    
    # Run the prerequisite checker, inheriting the current environment
    # This is important for tools installed in the current venv
    result = subprocess.run(
        [sys.executable, str(check_script)],
        cwd=str(ROOT),
        env=os.environ.copy()  # Inherit parent environment (important for venv PATH)
    )
    
    if result.returncode == 0:
        ok("All prerequisites are met!")
        return True
    else:
        print()
        banner("Prerequisites Not Met")
        print(c("red", "  ❌ Setup cannot proceed. Please install the missing prerequisites listed above."))
        print()
        print("  To verify prerequisites again, run:")
        print(f"    {c('bold', 'python scripts/check_prerequisites.py')} (any OS)")
        print(f"    {c('bold', './scripts/check_prerequisites.sh')} (macOS/Linux)")
        print(f"    {c('bold', '.\\\\scripts\\\\check_prerequisites.bat')} (Windows)")
        print()
        sys.exit(1)


def create_venv():
    """Create a Python virtual environment in .venv."""
    if VENV_DIR.exists():
        ok(f"Virtual environment already exists at {VENV_DIR.relative_to(ROOT)}")
        return
    info(f"Creating virtual environment at {VENV_DIR.relative_to(ROOT)}...")
    uv = find_uv()
    if uv:
        subprocess.run([uv, "venv", str(VENV_DIR)], check=True, cwd=str(ROOT))
    else:
        subprocess.run([sys.executable, "-m", "venv", str(VENV_DIR)], check=True)
    ok("Virtual environment created.")


def get_venv_python() -> str:
    """Get the path to the Python executable in the venv."""
    if sys.platform == "win32":
        return str(VENV_DIR / "Scripts" / "python.exe")
    else:
        return str(VENV_DIR / "bin" / "python")


def find_or_install_uv(venv_python: str) -> str:
    """Find uv or install it in the venv, fall back to pip."""
    # Try to find uv in PATH first
    uv = shutil.which("uv")
    if uv:
        return uv

    # Try home .local/bin on Unix
    home_uv = Path.home() / ".local" / "bin" / "uv"
    if home_uv.exists():
        return str(home_uv)

    # Try to find or install uv in venv
    info("Attempting to install 'uv' in virtual environment...")
    result = subprocess.run(
        [venv_python, "-m", "pip", "install", "-q", "uv"],
        capture_output=True
    )

    if result.returncode == 0:
        if sys.platform == "win32":
            uv_path = VENV_DIR / "Scripts" / "uv.exe"
        else:
            uv_path = VENV_DIR / "bin" / "uv"
        if uv_path.exists():
            ok("'uv' installed in virtual environment.")
            return str(uv_path)

    # Fall back to using pip directly
    warn("Could not install 'uv'. Will use pip for dependency management.")
    return None


def _remove_unifyroute_images():
    """Remove any locally-built Docker images whose name starts with 'unifyroute'.

    `docker compose down --rmi all` only removes images that compose itself
    manages (e.g. redis).  Images built from project Dockerfiles and
    tagged unifyroute* must be removed separately.
    """
    result = subprocess.run(
        ["docker", "images", "--filter", "reference=unifyroute*",
         "--format", "{{.Repository}}:{{.Tag}}"],
        capture_output=True, text=True
    )
    image_ids = [line.strip() for line in result.stdout.splitlines() if line.strip()]
    if not image_ids:
        info("No locally-built unifyroute* images found.")
        return
    for img in image_ids:
        info(f"Removing image: {img}")
        subprocess.run(["docker", "rmi", "-f", img], capture_output=False)


def _remove_unifyroute_containers():
    """Force-remove ALL containers (running or stopped) whose name starts with 'unifyroute'.

    `docker compose down` only removes containers it manages via the compose
    project.  Containers started outside compose or with a different project name
    must be cleaned up explicitly.
    """
    result = subprocess.run(
        ["docker", "ps", "-a",
         "--filter", "name=unifyroute",
         "--format", "{{.Names}}"],
        capture_output=True, text=True
    )
    names = [line.strip() for line in result.stdout.splitlines() if line.strip()]
    if not names:
        info("No unifyroute* containers found.")
        return
    for name in names:
        info(f"Removing container: {name}")
        subprocess.run(["docker", "rm", "-f", name], capture_output=False)


